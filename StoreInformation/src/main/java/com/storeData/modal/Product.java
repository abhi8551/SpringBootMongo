package com.storeData.modal;

public class Product {
	String name;
	int quantity;
	int perPiece;
	int total;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getQuantity() {
		return quantity;
	}

	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}

	public int getPerPiece() {
		return perPiece;
	}

	public void setPerPiece(int perPiece) {
		this.perPiece = perPiece;
	}

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

}
